            <p class="subtitle">Who am I?</p>
            <p class="textbox">
                Born: South East London in 1984.
                <br>
                <br>
                Early Education: I left school at 16 to attend a WestMinster KingsWay College in London to allow myself the opportunity to take 4 A-Levels. 4 As (The 
                highest grades available at the time) in Math. Physics, Chemistry and Biology followed in 2002.
                <br>
                <br>
                First Career: Given my love of animals I decided to persue a career as a vet. I was told at the time that as I went to an inner London college my chances 
                of achieving this were low, but never having been one to take being told something can't be done at face value I decided that taking a year out to 
                confirm my grades and gain some further experience would lean things in my favour. This proved to be the case and the following year (Sept. 2003) 
                I started my studies at the Royal Veterinary College (RVC).
            </p>
            <p class="subtitle">University and work until now</p>
            <p class="textbox">
                During my studies at the RVC I spent 12 months at Imperial College on an intercalated BSc course achieving a BSc in Medical Sciences with Molecular
                Biology and Cellular Biology with a 2:1 grade with honours. Returning to the RVC I completed my veterinary studies in 2009 and graduated with a 
                Bachelor of Veterinary Medicine. I preceeded from there to work as a first opinion veterinarian for 10 years.
                <br>
                <br>
                After 7 years of practice and feeling dissatisfied with my career I under took a post graduate certificate in Small Animal Surgery gaining the certificate in 2016. 
                Despite this and many other attempts at increasing my work satisfaction I continued to be unhappy at work and eventually in December of 2018 I decided that the 
                career I had worked so hard to achieve was no longer something I wished to continue.
            </p>
            <p class="subtitle">A New career</p>
            <p class="textbox">
                In the September of 2018 the student veterinary nurse of the practice was struggling with her studies of drug calculations and asked if I could write her some 
                questions on the various topics. Having dabbled in web coding previously with HTML and CSS I offered to write a simple program to generate questions and answers 
                for her in Python. At that point I became hooked on coding and kept improving the code I had written with every new piece of knowledge I coud find. Moving from 
                procedural to functional and eventually object orientated approaches.
                <br>
                <br>
                Having made the decision to leave veterinary practice for good it seemed that I had found a new direction in coding and part of me wonders if finding this has 
                lead to the decision being made. Whilst continuing to work out my notice as a vet I signed up to the Harvard University distance learning introduction to computer 
                science to try to fill in the gaps in my knowledge and skills from those that I had developed myself. Discovering not only the joy of coding something that would 
                do exactly what I expected it to do but also learning of the frustration when I was not quite close enough for the computer to carry out the required 
                processes and deciphering error message after error message. 
                <br>
                <br>
                The culmination of my Harvard course was the Veterinary Calculations Practice which is in fact a web based version of the coding project which hooked me 
                to coding in the first place. Since completing the course I am looking for my first development position and as of July 2019 I am currently unemployed whilst the 
                job search continues.
            </p>
